num_in_binary = 0B11
print("Num in binary 11=>", num_in_binary) #Num in binary 11=> 3
print("32 in binary=>", bin(32)) #32 in binary=> 0b100000

num_in_octal = 0O17
print("Num in octal 0O17=>", num_in_octal) #Num in octal 0O17=> 15
print("32 in oct=>", oct(32)) #32 in oct=> 0o40

num_in_hexa = 0X10
print("Num in hexa 0X10=>", num_in_hexa) #Num in hexa 0X10=> 16
print("32 in hexa=>", hex(32)) #32 in hexa=> 0x20