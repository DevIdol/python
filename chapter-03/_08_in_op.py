str = "Hello"
print("'Hell' in str:", "Hell" in str)  # 'Hell' in str: True

lst = [10, 20, 30]
print("10 in lst:", 10 in lst)  # 10 in lst: True

my_dict = {"name": "Joe", "age": 20}
print("name in my_dict:", "name" in my_dict)  # name in my_dict: True

my_tuple = ("js", "python")
print("python in my_tuple", "python" in my_tuple)  # python in my_tuple True

my_tuple = ("Java", "JS")
print("python not in my_tuple", "python" not in my_tuple)  # python not in my_tuple True
